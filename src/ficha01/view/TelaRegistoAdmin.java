    private void pfSenhaCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pfSenhaCKeyTyped

        String senha = new String(pfSenha.getPassword());
        if (!controller.validarSenha(senha)) {
            JOptionPane.showMessageDialog(null, "Senha inválida! A senha deve ter entre 6 e 16 caracteres e conter apenas letras e números.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

    }//GEN-LAST:event_pfSenhaCKeyTyped

