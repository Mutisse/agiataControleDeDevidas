    private void btnRegistar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistar1ActionPerformed

        // Validação dos campos
        char[] senhaChars = pfSenha.getPassword();
        String senha = new String(senhaChars);
        char[] comfirmsenha = pfSenhaC.getPassword();
        String confirmarSenha = new String(comfirmsenha);

        if (controller.camposNaoPreenchidos(txtNome.getText(), txtApelido.getText(), txtUser.getText(), senha, confirmarSenha)) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.", "", JOptionPane.ERROR_MESSAGE);
        } else if (!controller.senhaIgualConfirmarSenha(senha, confirmarSenha)) {
            JOptionPane.showMessageDialog(null, "A senha e a confirmação de senha devem ser iguais.", "", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Campos preenchidos corretamente.", "", JOptionPane.PLAIN_MESSAGE);
            this.dispose();
            new TelaLogin().setVisible(true);
        }

    }//GEN-LAST:event_btnRegistar1ActionPerformed

